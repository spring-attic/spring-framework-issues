package org.springframework.web.issues;

import javax.validation.Valid;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.validation.BindingResult;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

@Controller
public class RegistrationController {

	@RequestMapping(value="/registrations/add", method=RequestMethod.GET)
	public String add(Model model) {
		model.addAttribute(new Registration());
		return "addForm";
	}
	
	@RequestMapping(value="/registrations", method=RequestMethod.POST)
	public String save(@Valid Registration registration, BindingResult result) {
		return "addForm";
	}
	
}
